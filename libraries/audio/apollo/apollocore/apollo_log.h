/******************************************************
 *                   Enumerations
 ******************************************************/

typedef enum
{
    APOLLO_LOG_OFF = 0,
    APOLLO_LOG_ERR,
    APOLLO_LOG_WARNING,
    APOLLO_LOG_NOTICE,
    APOLLO_LOG_INFO,
    APOLLO_LOG_DEBUG0,
    APOLLO_LOG_DEBUG1,
    APOLLO_LOG_DEBUG2,
    APOLLO_LOG_DEBUG3,
    APOLLO_LOG_DEBUG4,

    APOLLO_LOG_PRINTF, /* Identifies log messages generated by apollo_log_printf calls */

    APOLLO_LOG_MAX
} APOLLO_LOG_LEVEL_T;

/******************************************************
 *                 Type Definitions
 ******************************************************/

typedef int (*log_output)(APOLLO_LOG_LEVEL_T level, char *logmsg);

