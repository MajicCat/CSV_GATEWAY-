wiced_result_t  apollo_log_set_platform_output(log_output platform_output);
wiced_result_t  apollo_log_set_level(APOLLO_LOG_LEVEL_T level);
APOLLO_LOG_LEVEL_T apollo_log_get_level(void);
wiced_result_t  apollo_log_msg(APOLLO_LOG_LEVEL_T level, const char *fmt, ...);
wiced_result_t  apollo_log_printf(const char *fmt, ...);
